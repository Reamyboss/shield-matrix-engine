"""
database.py — Shield Matrix Engine
All database operations. SQLite, no ORM, pure SQL.
"""

import sqlite3
import json
import os
from datetime import datetime

DB_PATH = os.environ.get("DB_PATH", "shield_matrix.db")


def get_conn():
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn


def init_db():
    """Create all tables if they don't exist."""
    # FIX 4: Use context manager so connection closes even if a CREATE TABLE throws
    with get_conn() as conn:
        c = conn.cursor()

        # All WC2026 fixtures (source of truth)
        c.execute("""
        CREATE TABLE IF NOT EXISTS fixtures (
            id          INTEGER PRIMARY KEY AUTOINCREMENT,
            match_id    TEXT UNIQUE,          -- e.g. "2026-06-11-MEX-RSA"
            date        TEXT NOT NULL,
            time_utc    TEXT,
            home_team   TEXT NOT NULL,
            away_team   TEXT NOT NULL,
            group_name  TEXT,
            venue       TEXT,
            round       TEXT,
            status      TEXT DEFAULT 'UPCOMING',  -- UPCOMING / LIVE / FINISHED
            home_score  INTEGER,
            away_score  INTEGER,
            ht_home     INTEGER,
            ht_away     INTEGER,
            goals_json  TEXT,                 -- JSON list of goal events
            updated_at  TEXT
        )""")

        # Predictions generated by the engine
        c.execute("""
        CREATE TABLE IF NOT EXISTS predictions (
            id              INTEGER PRIMARY KEY AUTOINCREMENT,
            match_id        TEXT NOT NULL,
            home_team       TEXT NOT NULL,
            away_team       TEXT NOT NULL,
            match_date      TEXT NOT NULL,
            home_win_pct    REAL,
            draw_pct        REAL,
            away_win_pct    REAL,
            home_xg         REAL,
            away_xg         REAL,
            over25_pct      REAL,
            under25_pct     REAL,
            btts_yes_pct    REAL,
            btts_no_pct     REAL,
            top_scorelines  TEXT,             -- JSON
            best_bet        TEXT,
            best_bet_pct    REAL,
            confidence      TEXT,
            created_at      TEXT,
            FOREIGN KEY (match_id) REFERENCES fixtures(match_id)
        )""")

        # Team strength ratings (updated from real WC2026 results)
        c.execute("""
        CREATE TABLE IF NOT EXISTS team_stats (
            team_name   TEXT PRIMARY KEY,
            played      INTEGER DEFAULT 0,
            wins        INTEGER DEFAULT 0,
            draws       INTEGER DEFAULT 0,
            losses      INTEGER DEFAULT 0,
            goals_for   INTEGER DEFAULT 0,
            goals_against INTEGER DEFAULT 0,
            points      INTEGER DEFAULT 0,
            avg_gf      REAL DEFAULT 0.0,
            avg_ga      REAL DEFAULT 0.0,
            group_name  TEXT,
            updated_at  TEXT
        )""")

        conn.commit()
    print("[DB] Tables ready.")


