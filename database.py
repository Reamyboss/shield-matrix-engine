def upsert_fixture(f: dict):
    # FIX 1: Context manager — connection always closes
    with get_conn() as conn:
        conn.execute("""
        INSERT INTO fixtures
            (match_id, date, time_utc, home_team, away_team, group_name, venue, round,
             status, home_score, away_score, ht_home, ht_away, goals_json, updated_at)
        VALUES
            (:match_id,:date,:time_utc,:home_team,:away_team,:group_name,:venue,:round,
             :status,:home_score,:away_score,:ht_home,:ht_away,:goals_json,:updated_at)
        ON CONFLICT(match_id) DO UPDATE SET
            status      = excluded.status,
            home_score  = excluded.home_score,
            away_score  = excluded.away_score,
            ht_home     = excluded.ht_home,
            ht_away     = excluded.ht_away,
            goals_json  = excluded.goals_json,
            updated_at  = excluded.updated_at
        """, f)
        conn.commit()


def get_fixtures_today(today: str) -> list:
    # FIX 1: Context manager — connection always closes
    with get_conn() as conn:
        rows = conn.execute(
            "SELECT * FROM fixtures WHERE date = ? ORDER BY time_utc", (today,)
        ).fetchall()
    return [dict(r) for r in rows]


def get_fixtures_upcoming(from_date: str, limit: int = 20) -> list:
    # FIX 1: Context manager — connection always closes
    with get_conn() as conn:
        rows = conn.execute(
            "SELECT * FROM fixtures WHERE date >= ? AND status = 'UPCOMING' ORDER BY date, time_utc LIMIT ?",
            (from_date, limit)
        ).fetchall()
    return [dict(r) for r in rows]


