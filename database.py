def rebuild_team_stats():
    """Recompute all team stats from finished fixtures."""
    conn = get_conn()
    finished = conn.execute(
        "SELECT * FROM fixtures WHERE status = 'FINISHED'"
    ).fetchall()

    stats = {}
    groups = {}

    for m in finished:
        h, a = m["home_team"], m["away_team"]
        gh, ga = m["home_score"], m["away_score"]
        grp = m["group_name"] or ""

        for t in [h, a]:
            if t not in stats:
                stats[t] = {"played":0,"wins":0,"draws":0,"losses":0,"gf":0,"ga":0}
            groups[t] = grp

        stats[h]["played"] += 1; stats[h]["gf"] += gh; stats[h]["ga"] += ga
        stats[a]["played"] += 1; stats[a]["gf"] += ga; stats[a]["ga"] += gh

        if gh > ga:
            stats[h]["wins"] += 1; stats[a]["losses"] += 1
        elif gh == ga:
            stats[h]["draws"] += 1; stats[a]["draws"] += 1
        else:
            stats[a]["wins"] += 1; stats[h]["losses"] += 1

    now = datetime.utcnow().isoformat()
    for team, s in stats.items():
        pts = s["wins"] * 3 + s["draws"]
        p = s["played"]
        conn.execute("""
        INSERT OR REPLACE INTO team_stats
            (team_name, played, wins, draws, losses, goals_for, goals_against,
             points, avg_gf, avg_ga, group_name, updated_at)
        VALUES (?,?,?,?,?,?,?,?,?,?,?,?)
        """, (team, p, s["wins"], s["draws"], s["losses"],
              s["gf"], s["ga"], pts,
              round(s["gf"]/p, 2) if p else 0,
              round(s["ga"]/p, 2) if p else 0,
              groups.get(team,""), now))

    conn.commit()
    conn.close()


def get_standings() -> dict:
    conn = get_conn()
    rows = conn.execute(
        "SELECT * FROM team_stats ORDER BY group_name, points DESC, goals_for DESC"
    ).fetchall()
    conn.close()

    groups = {}
    for r in rows:
        r = dict(r)
        g = r["group_name"] or "Other"
        if g not in groups:
            groups[g] = []
        groups[g].append(r)
    return groups


